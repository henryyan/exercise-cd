package net.yanhl.field.exception;

/**
 * <p><b>Title：</b> 场地活动异常</p>
 * <p><b>Description：</b></p>
 * 
 * @author 闫洪磊
 * @since  1.0
 * @version 1.0.0.20091013
 */
public class FieldActivityException extends Exception {

	private static final long serialVersionUID = 1L;

	public FieldActivityException() {
		super();
	}

	public FieldActivityException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public FieldActivityException(String arg0) {
		super(arg0);
	}

	public FieldActivityException(Throwable arg0) {
		super(arg0);
	}

}
