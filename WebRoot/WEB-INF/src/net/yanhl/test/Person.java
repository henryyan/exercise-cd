package net.yanhl.test;

import java.sql.Date;

public class Person {   
    private String name;    
    private Date birthday;    
  
    public void setName(String name) {    
        this.name = name;    
    }    
  
    public String getName() {    
        return name;    
    }    
  
    public void setBirthday(Date birthday) {    
        this.birthday = birthday;    
    }    
  
    public Date getBirthday() {    
        return birthday;    
    }    
  
}  